package com.karthi.ShapeApp.Shape.imp;

import com.karthi.ShapeApp.Shape.Dot;

public class Point implements Dot {
    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "(" + x + ", " + y + ")";
    }
}
